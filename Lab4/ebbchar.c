static ssize_t dev_write(struct file *filep, const char *buffer, size_t len, loff_t *offset){
    unsigned long p =  *offset;
    unsigned int count = len;
    int ret = 0;
    // struct mem_dev *dev = filep->private_data; /*获得设备结构体指针*/
  
    /*分析和获取有效的写长度*/
    if (p >= MSG_SIZE)
       return 0;
    if (count > MSG_SIZE - p)    /*要写入的字节大于设备的内存空间*/
       count = MSG_SIZE - p;

    /*从用户空间写入数据*/
    if (copy_from_user(message + p, buffer, count))
       ret =  - EFAULT;
    else{
       *offset += count;      /*增加偏移位置*/  
       ret = count;      /*返回实际的写入字节数*/ 
       printk(KERN_INFO "EBBChar: written %d bytes(s) from %d\n", count, p);
    }

    return ret; 
}

/** @brief The device release function that is called whenever the device is closed/released by
 *  the userspace program
 *  @param inodep A pointer to an inode object (defined in linux/fs.h)
 *  @param filep A pointer to a file object (defined in linux/fs.h)
 */
static int dev_release(struct inode *inodep, struct file *filep){
   printk(KERN_INFO "EBBChar: Device successfully closed\n");
   return 0;
}

/** @brief A module must use the module_init() module_exit() macros from linux/init.h, which
 *  identify the initialization function at insertion time and the cleanup function (as
 *  listed above)
 */
module_init(ebbchar_init);
module_exit(ebbchar_exit);